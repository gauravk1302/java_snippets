public class multidimensional_array {
    public static void main(String[] args) {
        int[][] myNumbers = { {1, 2, 3, 4}, {5, 6, 7} };

        System.out.println(myNumbers[1][2]);

        //to change the value of any element 
        myNumbers[1][2] =9;
        System.out.println(myNumbers[1][2]);

        
    }
}
